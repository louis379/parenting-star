// ── 工具函數 ─────────────────────────────────────────────────

function getDataTable(gender: Gender, measurement: Measurement): PercentileRow[] {
  if (measurement === 'height') return gender === 'male' ? HEIGHT_MALE : HEIGHT_FEMALE
  if (measurement === 'weight') return gender === 'male' ? WEIGHT_MALE : WEIGHT_FEMALE
  return gender === 'male' ? HEAD_MALE : HEAD_FEMALE
}

/**
 * 線性插值：在兩個已知數據點之間估算值
 */
function interpolate(x: number, x0: number, y0: number, x1: number, y1: number): number {
  if (x1 === x0) return y0
  return y0 + ((y1 - y0) * (x - x0)) / (x1 - x0)
}

/**
 * 取得指定月齡的百分位數參考值
 * @param gender 性別
 * @param ageMonths 月齡
 * @param measurement 測量項目
 * @returns 七個百分位點 [P3, P10, P25, P50, P75, P90, P97]，若月齡超出範圍回傳 null
 */
export function getPercentileValues(
  gender: Gender,
  ageMonths: number,
  measurement: Measurement,
): { p3: number; p10: number; p25: number; p50: number; p75: number; p90: number; p97: number } | null {
  const table = getDataTable(gender, measurement)
  const age = Math.round(ageMonths)

  // 找最近的兩個數據點以進行插值
  let lower: PercentileRow | null = null
  let upper: PercentileRow | null = null

  for (const row of table) {
    if (row[0] <= age) lower = row
    if (row[0] >= age && !upper) upper = row
  }

  if (!lower && !upper) return null
  if (!lower) return { p3: upper![1], p10: upper![2], p25: upper![3], p50: upper![4], p75: upper![5], p90: upper![6], p97: upper![7] }
  if (!upper || lower[0] === upper[0]) return { p3: lower[1], p10: lower[2], p25: lower[3], p50: lower[4], p75: lower[5], p90: lower[6], p97: lower[7] }

  const lerp = (i: number) => interpolate(age, lower![0], lower![i], upper![0], upper![i])
  return { p3: lerp(1), p10: lerp(2), p25: lerp(3), p50: lerp(4), p75: lerp(5), p90: lerp(6), p97: lerp(7) }
}

/**
 * 計算某測量值對應的百分位數（0–100）
 * @param gender 性別
 * @param ageMonths 月齡
 * @param measurement 測量項目
 * @param value 實測值
 * @returns 估算的百分位數（0–100），若無法計算回傳 null
 */
export function getPercentile(
  gender: Gender,
  ageMonths: number,
  measurement: Measurement,
  value: number,
): number | null {
  const refs = getPercentileValues(gender, ageMonths, measurement)
  if (!refs) return null

  const points: [number, number][] = [
    [3, refs.p3],
    [10, refs.p10],
    [25, refs.p25],
    [50, refs.p50],
    [75, refs.p75],
    [90, refs.p90],
    [97, refs.p97],
  ]

  // 超出範圍的極值
  if (value <= points[0][1]) return 3
  if (value >= points[points.length - 1][1]) return 97

  // 在兩個百分位點之間插值
  for (let i = 0; i < points.length - 1; i++) {
    const [p0, v0] = points[i]
    const [p1, v1] = points[i + 1]
    if (value >= v0 && value <= v1) {
      return Math.round(interpolate(value, v0, p0, v1, p1))
    }
  }

  return null
}

/**
 * 取得某月齡的預期生長數值（P50 中位數）
 * @returns 各測量項目的中位數（身高/體重/頭圍），若無資料則為 null
 */
export function getExpectedGrowth(gender: Gender, ageMonths: number): {
  height_cm: number | null
  weight_kg: number | null
  head_circumference_cm: number | null
} {
  const height = getPercentileValues(gender, ageMonths, 'height')
  const weight = getPercentileValues(gender, ageMonths, 'weight')
  const head = getPercentileValues(gender, ageMonths, 'head')

  return {
    height_cm: height?.p50 ?? null,
    weight_kg: weight?.p50 ?? null,
    head_circumference_cm: head?.p50 ?? null,
  }
}

/**
 * 取得成長曲線數據（用於 Recharts 折線圖）
 * 回傳 0 到指定月齡範圍的各百分位數據點
 */
export function getGrowthCurveData(
  gender: Gender,
  measurement: Measurement,
  maxAgeMonths = 60,
): Array<{
  age: number
  p3: number
  p10: number
  p25: number
  p50: number
  p75: number
  p90: number
  p97: number
}> {
  const table = getDataTable(gender, measurement)
  return table
    .filter(row => row[0] <= maxAgeMonths)
    .map(([age, p3, p10, p25, p50, p75, p90, p97]) => ({
      age, p3, p10, p25, p50, p75, p90, p97,
    }))
}

/**
 * 百分位數的中文描述標籤
 */
export function getPercentileLabel(percentile: number | null): string {
  if (percentile === null) return '未知'
  if (percentile < 3) return '偏低（< P3）'
  if (percentile < 10) return '略低（P3–P10）'
  if (percentile < 25) return '中下（P10–P25）'
  if (percentile < 75) return '正常（P25–P75）'
  if (percentile < 90) return '中上（P75–P90）'
  if (percentile < 97) return '略高（P90–P97）'
  return '偏高（> P97）'
}

/**
 * 依百分位數回傳顏色（用於 UI 顯示）
 */
export function getPercentileColor(percentile: number | null): string {
  if (percentile === null) return '#9ca3af'
  if (percentile < 3 || percentile > 97) return '#ef4444'   // 紅
  if (percentile < 10 || percentile > 90) return '#f97316'  // 橘
  if (percentile < 25 || percentile > 75) return '#eab308'  // 黃
  return '#22c55e'                                           // 綠
}
